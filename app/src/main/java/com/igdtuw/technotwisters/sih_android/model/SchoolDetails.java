public class SchoolDetails {
    String status;
    String message;
    double latitude;
    double longitude;
    String school_name;

    public String getStatus() {
        return status;
    }

    public String getMessage() {
        return message;
    }

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public String getSchoolName() {
        return school_name;
    }
}
